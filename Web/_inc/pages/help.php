<h1>Comandos y Mapas</h1><p>Estos son todos los comandos que el administrador de un servidor puede utilizar: 
<br/>
/admin xxx (siendo xxx la contrase&ntilde;a designada en la ventana del Server.exe)<br/>
/activar bots (activa personajes manejados por la computadora en ambos bandos)<br/>

/activar resu (activa el hechizo Resucitar)<br/>
/activar estu (activa e hechizo Estupidez)<br/>
/activar invi (activa el hechizo Invisibilidad)<br/>
/activar fatuos (Activa el hechizo Implorar ayuda)<br/>
/activar fuegoaliado (Activa el friendly-fire o "fuego aliado"<br/>

/activar deathmatch (activa el modo deathmatch)<br/>
/restart (reinicia la partida)<br/>
/mapa # (cambiamos # por el n&uacute;mero de mapa que deseamos en nuestro servidor)<br/>

/echar xxx (siendo xxx el nombre del usuario que queremos echar de nuestro servidor)<br/><br/>

Estos comandos est&aacute;n pensados para que el administrador no deba abrir la ventana del servidor cada vez que desea cambiar algo.<br/><br/>
Todos los comandos pueden ser usados de la misma manera pero poniendo /desactivar en vez de /activar (por ejemplo para desactivar invisibilidad ponemos /desactivar invi)<br/><br/>
Tambi&eacute;n sirven para los administradores que no tienen acceso a la ventana del server, que s&iacute; tienen acceso a estos comandos<br/><br/>

